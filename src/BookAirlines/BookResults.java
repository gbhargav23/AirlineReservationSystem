package BookAirlines;

import java.sql.Date;

public class BookResults {

	private String flightId;
	private String departuteDate;
	private String source;
	private String destination;
	private String flightClass;
	
	public String getFlightId() {
		return flightId;
	}
	public void setFlightId(String flightId) {
		this.flightId = flightId;
	}
	public String getDepartuteDate() {
		return departuteDate;
	}
	public void setDepartuteDate(String departuteDate) {
		this.departuteDate = departuteDate;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public String getFlightClass() {
		return flightClass;
	}
	public void setFlightClass(String flightClass) {
		this.flightClass = flightClass;
	}
	
	
}
